#include <stdio.h>
#include <stdlib.h>
#include <signal.h>
#include <unistd.h>

//------------------------------------------------------------------------------
// Function Prototypes
//------------------------------------------------------------------------------
void HandleUSR1(int signum);

//------------------------------------------------------------------------------
// Main Program
//------------------------------------------------------------------------------
int main(void) {

    signal(SIGINT, SIG_IGN);
    signal(SIGUSR1, HandleUSR1);

    while (1) {
        pause();
    }

    return EXIT_SUCCESS;
}

//------------------------------------------------------------------------------
// Function HandleUSR1
//------------------------------------------------------------------------------
void HandleUSR1(int signum) {
    printf("SignalReceiver just got an SIGUSR1\n");
    fflush(stdout);
}

